#include <stdio.h>  // for input/output

int main() {
    int radius;
    // Ask user to enter radius
    printf("Enter radius of circle: ");
    scanf("%d", &radius);

    // Calculate area
    int area = 3.14 * radius * radius;

    // Display Result
    printf("Area of ur boobs is = %d\n", area);
    return 0;  // tells the OS the program ended successfully
}

